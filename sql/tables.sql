CREATE TABLE USERS 
(
	USER_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	USERNAME VARCHAR(50) NOT NULL,
	NORMALIZED_USERNAME VARCHAR(50) DEFAULT '' NOT NULL,
	VORNAME VARCHAR(50) DEFAULT '' NOT NULL,
	NACHNAME VARCHAR(50) DEFAULT '' NOT NULL,
	ACTIVE_DIRECTORY_GUID VARCHAR(36),
	EMAIL VARCHAR(255) DEFAULT '' NOT NULL,
	PASSWORD VARCHAR(255) DEFAULT '' NOT NULL,
	SALT VARCHAR(255) DEFAULT '' NOT NULL,
	ACCOUNT_TYPE VARCHAR(20) DEFAULT 'LocalAccount' NOT NULL,
	IS_AKTIV BOOLEAN DEFAULT FALSE NOT NULL,
	IS_ADMIN BOOLEAN DEFAULT FALSE NOT NULL,
	CONSTRAINT USERS_PK PRIMARY KEY (USER_ID)
);

CREATE TABLE ABTEILUNGEN 
(
	ABTEILUNG_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	NAME VARCHAR(50) NOT NULL,
	ACTIVE_DIRECTORY_GROUP_CN VARCHAR(255) DEFAULT '' NOT NULL,
	CONSTRAINT ABTEILUNGEN_PK PRIMARY KEY (ABTEILUNG_ID)
);

CREATE TABLE ERP_CONFIG 
(
	CONFIG_KEY VARCHAR(100) NOT NULL,
	CONFIG_VALUE VARCHAR(255) NOT NULL,
	CONSTRAINT CONFIG_PK PRIMARY KEY (CONFIG_KEY)
);

CREATE TABLE LANDESKENNZEICHEN 
(
	LAND_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	NAME VARCHAR(30) NOT NULL,
	ISO2 VARCHAR(2) NOT NULL,
	ISO3 VARCHAR(3) NOT NULL,
	VORWAHL VARCHAR(5) DEFAULT '' NOT NULL,
	CONSTRAINT LANDESKENNZEICHEN_PK PRIMARY KEY (LAND_ID),
	CONSTRAINT LANDESKENNZEICHEN_UK_ISO2 UNIQUE (ISO2),
	CONSTRAINT LANDESKENNZEICHEN_UK_ISO3 UNIQUE (ISO3)
);

CREATE TABLE SPRACHEN 
(
	SPRACH_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	NAME VARCHAR(30) NOT NULL,
	CONSTRAINT SPRACHEN_PK PRIMARY KEY (SPRACH_ID)
);

CREATE TABLE UEBERSETZUNGEN 
(
	CODE VARCHAR(50) NOT NULL,
	SPRACH_ID INTEGER NOT NULL,
	PARENT_ID INTEGER NOT NULL,
	VALUE_TEXT VARCHAR(255),
	VALUE_BLOB BLOB SUB_TYPE TEXT,
	CONSTRAINT UEBERSETZUNGEN_PK PRIMARY KEY (CODE,PARENT_ID,SPRACH_ID),
	CONSTRAINT UEBERSETZUNGEN_FK_SPRACH_ID FOREIGN KEY (SPRACH_ID) REFERENCES SPRACHEN(SPRACH_ID)
);

CREATE TABLE ANREDEN 
(
	ANREDE_ID INTEGER GENERATED BY DEFAULT AS IDENTITY NOT NULL,
	NAME VARCHAR(50),
    LETZTER_BEARBEITER INTEGER DEFAULT NULL,
    ZULETZT_GEAENDERT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	CONSTRAINT PK_ANREDEN PRIMARY KEY (ANREDE_ID),
    CONSTRAINT FK_ANREDEN_USER FOREIGN KEY (LETZTER_BEARBEITER) REFERENCES USERS(USER_ID) ON UPDATE CASCADE ON DELETE SET NULL
);
