    CONSTRAINT FK_CONFIGURATION_CURRENCY FOREIGN KEY (CUSTOMER_CURRENCY_CODE) REFERENCES CURRENCIES(CODE) ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT FK_CONFIGURATION_COUNTRY FOREIGN KEY (CUSTOMER_COUNTRY_ID) REFERENCES COUNTRIES(COUNTRY_ID) ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT FK_CONFIGURATION_LANGUAGE FOREIGN KEY (CUSTOMER_LANGUAGE_ID) REFERENCES LANGUAGES(LANGUAGE_ID) ON UPDATE CASCADE ON DELETE SET NULL,
    CONSTRAINT FK_CONFIGURATION_USER FOREIGN KEY (LAST_MODIFIED_BY) REFERENCES USERS(USER_ID) ON UPDATE CASCADE ON DELETE SET NULL
